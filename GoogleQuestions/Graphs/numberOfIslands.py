from collections import defaultdict

class Solution:
    def numIslands(self, grid) -> int:
        def BFSHelper(rowInd, colInd, lenRows, lenCols):
            Q = [(rowInd,colInd)]

            while Q:
                currRow, currCol = Q.pop(0)
                if currRow >= 0 and currRow < len(grid) and currCol >= 0 and currCol < len(grid[0]) and grid[currRow][currCol] == '1':
                    Q += ((currRow + 1, currCol), (currRow - 1, currCol), (currRow, currCol + 1), (currRow, currCol - 1))
                    grid[currRow][currCol] = '0'

        if not grid:
            return 0

        numIslands = 0

        rows = len(grid)
        cols = len(grid[0])

        for rowInd in range(rows):
            for colInd in range(cols):
                if grid[rowInd][colInd] == '1':
                    numIslands += 1
                    BFSHelper(rowInd, colInd, rows, cols)

        return numIslands

